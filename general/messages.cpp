#include "messages.h"

Messages::Messages() {}

// default
Msg Messages::m_nextMessage = No_message;

void Messages::print() {
  switch (m_nextMessage) {
    case Welcome: cout << p_welcome_message; break;
    case Start: cout << p_start_message; break;
    case GameOver: cout << p_game_over; break;
    default:
      cout << "nothing to print" << endl;
    break;
  }
}

