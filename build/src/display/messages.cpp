#include "headers/messages.h"

Messages::Messages() {}

// default
Msg Messages::m_nextMessage = No_message;

void Messages::print() {
  switch (m_nextMessage) {
    case Welcome: cout << p_welcome_message; break;
    case ChooseStartOption: cout << p_options_message; break;
    case GameOver: cout << p_game_over; break;
    case ChooseColor: cout << p_choose_color_message; break;
    default:
                      cout << "nothing to print" << endl;
                      break;
  }
}

